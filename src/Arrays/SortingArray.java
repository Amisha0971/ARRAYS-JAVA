package Arrays;

import java.util.Arrays;

public class SortingArray {

    public static void main(String[] args) {

        int[] numbers = {5, 1, 4, 2, 3};

        // Sort the array
        Arrays.sort(numbers);

        // Print the sorted array
        System.out.println("Sorted Array: " + Arrays.toString(numbers));
    }
}
